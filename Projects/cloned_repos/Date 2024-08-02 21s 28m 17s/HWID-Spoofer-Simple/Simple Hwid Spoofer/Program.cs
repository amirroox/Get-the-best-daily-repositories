    static string RandomMacAddress()
    {
        Random random = new Random();
        byte[] macAddressBytes = new byte[6];
        random.NextBytes(macAddressBytes);

        // Set the "locally administered" bit and clear the "unicast" bit
        macAddressBytes[0] = (byte)(macAddressBytes[0] | (byte)2);

        return string.Join(":", macAddressBytes.Select(b => b.ToString("X2")));
    }

    static bool ChangeMacAddressForInterface(string networkInterfaceName, string newMacAddress)
    {
        try
        {
            NetworkInterface[] networkInterfaces = NetworkInterface.GetAllNetworkInterfaces();
            foreach (NetworkInterface networkInterface in networkInterfaces)
            {
                if (networkInterface.Name == networkInterfaceName)
                {
                    ManagementObjectSearcher mos = new ManagementObjectSearcher("SELECT * FROM Win32_NetworkAdapter WHERE NetConnectionId != NULL");
                    ManagementObjectCollection moc = mos.Get();
                    foreach (ManagementObject mo in moc)
                    {
                        if (mo["Name"].ToString() == networkInterface.Name)
                        {
                            ManagementBaseObject newMacAddressProperties = mo.GetMethodParameters("SetMACAddress");
                            newMacAddressProperties["MacAddress"] = newMacAddress;
                            ManagementBaseObject setMacAddress = mo.InvokeMethod("SetMACAddress", newMacAddressProperties, null);
                            if ((uint)setMacAddress.Properties["ReturnValue"].Value == 0)
                            {
                                return true;
                            }
                        }
                    }
                }
            }
        }
        catch (Exception ex)
        {
            Console.WriteLine("Error: " + ex.Message);
        }
        return false;
    }

